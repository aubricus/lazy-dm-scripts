def generate_names(given_names, surnames, length=1):
    _given_names = copy(given_names)
    _surnames = copy(surnames)

    shuffle(_given_names)
    shuffle(_surnames)

    return zip(
        _given_names[:length],
        _surnames[:length],
    )


def main(argv, given_names, surnames):
    description = __doc__
    formatter_class = argparse.RawDescriptionHelpFormatter
    parser = argparse.ArgumentParser(
        description=description, formatter_class=formatter_class
    )

    parser.add_argument("-l", "--length", default=10, type=int)

    args = parser.parse_args(argv)
    names = generate_names(given_names, surnames, args.length)

    for given_name, surname in names:
        print(f"{given_name} {surname}")


if __name__ == "__main__":
    argv = sys.argv[1:]
    sys.exit(main(argv, given_names, surnames))
